function hitKodeKelas()
{
	thnmasuk = document.frmTmbhKelas.txtThnMasuk.value;
	kodepk = document.frmTmbhKelas.txtKodePK.value;
	pararel = document.frmTmbhKelas.txtPararel.value;
	singkatan = document.frmTmbhKelas.txtSingkat.value;
	tingkat = document.frmTmbhKelas.txtTingkat.value;
	if(document.frmTmbhKelas.txtTingkat.value=='X')
		{AngkaTk='10'} else
	if(document.frmTmbhKelas.txtTingkat.value=='XI')
		{AngkaTk='11'} else
	if(document.frmTmbhKelas.txtTingkat.value=='XII')
		{AngkaTk='12'}

	kodekelas = thnmasuk+kodepk+pararel+'-'+tingkat+singkatan+pararel;
	kelasna = tingkat+' '+singkatan+' '+pararel;

	username = 'WK'+AngkaTk+singkatan+pararel;
	passname = 'WK'+AngkaTk+singkatan+pararel+thnmasuk;
	
	document.frmTmbhKelas.txtKodeKls.value = kodekelas;
	document.frmTmbhKelas.txtNamaKls.value = kelasna;
	document.frmTmbhKelas.txtUserWK.value = username;
	document.frmTmbhKelas.txtPassWK.value = passname;
}
function showCD(str)
{
if (str=='')
  {
  document.getElementById('txtHint').innerHTML='';
  return;
  }
if (window.XMLHttpRequest)
  {// code for IE7+, Firefox, Chrome, Opera, Safari
  xmlhttp=new XMLHttpRequest();
  }
else
  {// code for IE6, IE5
  xmlhttp=new ActiveXObject('Microsoft.XMLHTTP');
  }
xmlhttp.onreadystatechange=function()
  {
  if (xmlhttp.readyState==4 && xmlhttp.status==200)
	{
	document.getElementById('txtHint').innerHTML=xmlhttp.responseText;
	}
  }
xmlhttp.open('GET','pages/akademik-kelas-walikelas-cari.php?q='+str,true);
xmlhttp.send();
}
